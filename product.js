function dispaly()
{
    product.map(function(ele,index)
    {
    var off=document.createElement('p');
    var big_image=document.createElement('img');
    var small_image=document.createElement('img');
    var small=document.createElement('div');
    var big=document.createElement('div');
    var name=document.createElement('p');
    var defi=document.createElement('p');
    var color_name=document.createElement('p');
    var image2=document.createElement('img');
    var price=document.createElement('div');
    var actual_price=document.createElement('p');
    var deleted_price=document.createElement('del');
    var off=document.createElement('p');
    var button=document.createElement('button');
    button.innerHTML="add to cart";
    big_image.setAttribute("src",ele.image_url);
    big_image.setAttribute("alt",index);
    big_image.setAttribute("class",'image1');
    small_image.setAttribute("src",ele.sma_img);
    small_image.setAttribute("alt",index);
    small_image.setAttribute("class",'sma_img');
    image2.setAttribute("class",'image2');
    image2.setAttribute("src",ele.small_image);
    image2.setAttribute("alt",index);
    big.setAttribute("class","big");
    small.setAttribute("class","small");
    price.setAttribute("class","prices");
    name.setAttribute("class","name");
    defi.setAttribute("class","defi");
    color_name.setAttribute("class","color");
    actual_price.setAttribute("class","actual_price");
    deleted_price.setAttribute("class","deleted_price");
    button.setAttribute("class","button");
    button.addEventListener("click",function()
    {
      addtocart(index);
    });
    name.innerHTML=ele.pro_name;
    defi.innerHTML=ele.definition;
    color_name.innerHTML=ele.color;
    actual_price.innerHTML='₹'+ele.actual_price;
    deleted_price.innerHTML='₹'+ele.deleted_price;
    off.setAttribute("class","off");
    off.innerHTML='₹'+(ele.deleted_price-ele.actual_price);
    price.append(actual_price,deleted_price,off);
    small.append(name,defi,color_name,image2,price,button);
    big.append(big_image,small_image,small);
    

    document.getElementById('second').append(big);

   


    }
    )

}

var cartarr=JSON.parse(localStorage.getItem("cart"))||[];
function addtocart(i)
{
   var products=product.filter(function(ele,index)
   {
      return index==i;
   });
   cartarr.push(products[0]);
   console.log(products);
   localStorage.setItem("cart",JSON.stringify(cartarr));
}